class Array3{

    public static void main(String[] args){
        String[][] arr;
        arr = new String[2][2];
        
        arr[0][0] = "sato";
        arr[0][1] = "suzuki";
        arr[1][0] = "tanaka";
        arr[1][1] = "sakamoto";

        System.out.println(arr[0][0]);
        System.out.println(arr[0][1]);
        System.out.println(arr[1][0]);
        System.out.println(arr[1][1]);


    }
}